//  programs using Arithmetic Operator.

#include <iostream>
using namespace std;
 int main()
 {
 	int a,b,ans;
 	cout<<" Enter the  two Number : ";
	cin>>a >>b; 
 	ans = a+b;
 	cout << "\n Addition = " << ans;
 	
 	ans = a-b;
 	cout << "\n subtraction = " << ans;
 	
 	ans = a*b;
 	cout << "\n Multiplication = " << ans;
 	
 	ans = a%b;
 	cout << "\n modulo = " << ans;
 	
 	ans = a/b;
 	cout <<"\n  Division = " << ans;
     
     return 0;

 }

